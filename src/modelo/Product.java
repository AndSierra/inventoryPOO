
package modelo;

public class Product {
    private int keyPdt;
    private String nombrePdt;
    private int precioPdt;
    private int numUnidadesPdt;

//    getters and setters
    
    public int getKeyPdt() {
        return keyPdt;
    }

    public void setKeyPdt(int keyPdt) {
        this.keyPdt = keyPdt;
    }

    public String getNombrePdt() {
        return nombrePdt;
    }

    public void setNombrePdt(String nombrePdt) {
        this.nombrePdt = nombrePdt;
    }

    public int getPrecioPdt() {
        return precioPdt;
    }

    public void setPrecioPdt(int precioPdt) {
        this.precioPdt = precioPdt;
    }

    public int getNumUnidadesPdt() {
        return numUnidadesPdt;
    }

    public void setNumUnidadesPdt(int numUnidadesPdt) {
        this.numUnidadesPdt = numUnidadesPdt;
    }
    
    
    
}
