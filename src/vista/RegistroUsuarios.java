    private void jTablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTablaMouseClicked
        try {
            
            int fila = jTabla.getSelectedRow();
            int id =  Integer.parseInt(jTabla.getValueAt(fila, 0).toString());
            PreparedStatement ps;
            
            Connection con = connection.getconnection();
            ps = con.prepareStatement("SELECT nombre,apellido,cc,rol FROM usuarios WHERE id = ?");
            ps.setInt(1, id);
            rs = ps.executeQuery();

            while(rs.next()){
                txtId.setText(String.valueOf(id));
                txtNombre.setText(rs.getString("nombre"));
                txtApellido.setText(rs.getString("apellido")); 
                txtCc.setText(rs.getString("cc"));
                
                if (Integer.parseInt(rs.getString("rol"))== 1) {
                    rbAdmin.setSelected(true);
                }else{
                    if (Integer.parseInt(rs.getString("rol"))== 2) {
                     rbUsuario.setSelected(true);   
                    }
                }
               
            }
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jTablaMouseClicked

